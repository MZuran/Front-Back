// Ejemplo de ruta con params y queries
// http://localhost:4000/home/soy_un_param?primerQuery=soy_un_query&segundaQuery=soy_otra_query
app.get('/home/:param', (req, res) => {
	const params = req.params
	const query = req.query

	res.json(
		{
			message: 'Bienvenido a la página principal',
			param_enviado: params,
			query_enviada: query
		}
	);
});

io.on("connection", (socket) => {
	const req = socket.request;

	socket.on('joinRoom', data => {
		console.log("🚀 ~ io.on ~ req.session.room:", req.session.room)
		if (req.session.room != undefined && req.session.room.length > 0)
			socket.leave(req.session.room);
		req.session.room = data.room;
		socket.join(req.session.room);

		io.to(req.session.room).emit('chat-messages', { user: req.session.user, room: req.session.room });
	});

	socket.on('pingAll', data => {
		console.log("PING ALL: ", data);
		io.emit('pingAll', { event: "Ping to all", message: data });
	});

	socket.on('sendMessage', data => {
		io.to(req.session.room).emit('newMessage', { room: req.session.room, message: data });
	});

	socket.on('disconnect', () => {
		console.log("Disconnect");
	})
});